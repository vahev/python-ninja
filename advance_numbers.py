# Using of advanced numbers in Python

source_number = -100

# Convert our source number to binary system
bin_number = bin(source_number)

# Convert our source number to Hex system
hex_number = hex(source_number)

# Extracts the absolute value of a numbers
abs_number = abs(source_number)

# Rounds the number.The 2 argument indicates the decimal positions
rnd_number = round(source_number / 2, 2)
